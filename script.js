const url = new URL(window.location.href);
const params = new URLSearchParams(url.search);

const season = params.get("season");
const ep = params.get("ep");
const clip = params.get("clip");

const clipImg1 = document.querySelector("#img1");
const clipImg2 = document.querySelector("#img2");
const clipImg3 = document.querySelector("#img3");

const backButton = document.querySelector("[data-BackButton]");
const title = document.querySelector("[data-EPtitle]")
const video = document.querySelector("#video");
const desc = document.querySelector("#desc");
const descSection = document.querySelector("[data-EPDesc]");
const clipsSection = document.querySelector("[data-Clips]");
const clip1Title = document.querySelector("#clip1Title");
const clip2Title = document.querySelector("#clip2Title");

switch (season) {
    case "6":
        LoadEpisode(S6Episodes, "Seasons/Season6.html");
        break;

    case "special":
        LoadEpisode(Specials, "index.html");
        break;
}

function LoadEpisode(SeasonObject, hrefLink) {
    for (const episode in SeasonObject) {
        if (episode == ep) {
            const selectedEP = SeasonObject[episode];
            switch (parseInt(clip)) {
                case 0:
                    title.innerHTML = selectedEP.title;
                    video.src = selectedEP.video;
                    desc.innerHTML = selectedEP.desc;
                    clip1Title.innerHTML = selectedEP.clip1Title;
                    clip2Title.innerHTML = selectedEP.clip2Title;
                    clipImg1.src = selectedEP.clip1Icon;
                    clipImg2.src = selectedEP.clip2Icon;
                    clipImg3.src = selectedEP.clip3Icon;
                    clipImg1.parentElement.href = `episodePage.html?season=${season}&ep=${episode}&clip=1`;
                    clipImg2.parentElement.href = `episodePage.html?season=${season}&ep=${episode}&clip=2`;
                    clipImg3.parentElement.href = `episodePage.html?season=${season}&ep=${episode}&clip=3`;
                    break;

                case 1:
                    title.innerHTML = `${selectedEP.title} - ${selectedEP.clip1Title.toUpperCase()}`;
                    video.src = selectedEP.clip1;
                    descSection.remove();
                    clipsSection.remove();
                    break;

                case 2:
                    title.innerHTML = `${selectedEP.title} - ${selectedEP.clip2Title.toUpperCase()}`;
                    video.src = selectedEP.clip2;
                    descSection.remove();
                    clipsSection.remove();
                    break;

                case 3:
                    title.innerHTML = `${selectedEP.title} - CENA PRÉ-CRÉDITOS`;
                    video.src = selectedEP.clip3;
                    descSection.remove();
                    clipsSection.remove();
                    break;
            }
            backButton.addEventListener("click", () => {
                const link = backButton.parentElement;
                if (parseInt(clip) === 0) {
                    link.href = hrefLink;
                } else link.href = `episodePage.html?season=${season}&ep=${episode}&clip=0`;
            })
        }
    }
}